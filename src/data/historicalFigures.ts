export function getFigureById(id: string): HistoricalFigure | undefined {
  return historicalFigures.find((figure) => figure.id === id);
}

export function getRelatedFigures(figure: HistoricalFigure): HistoricalFigure[] {
  return figure.relations
    .map((relation) => getFigureById(relation.targetFigureId))
    .filter((item): item is HistoricalFigure => item !== undefined);
}
